public class PopcornPopper {

    public void on() {
        System.out.println("PopcornPopper  打开");
    }

    public void pop() {
        System.out.println("PopcornPopper  爆米花");
    }

    public void off() {
        System.out.println("PopcornPopper  关闭");
    }
}
